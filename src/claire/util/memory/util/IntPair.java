package claire.util.memory.util;

public class IntPair {
	
	private int obj;
	private int pair;

	public IntPair(int i1, int i2) 
	{
		this.obj = i1;
		this.pair = i2;
	}

	public int getObj()
	{
		return obj;
	}

	public void setObj(int obj)
	{
		this.obj = obj;
	}

	public int getPair()
	{
		return pair;
	}

	public void setPair(int pair)
	{
		this.pair = pair;
	}

}
